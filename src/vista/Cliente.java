    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        String dnip = dniField.getText();
        int respuesta = JOptionPane.showConfirmDialog(null, " ¿Esta seguro que desea elminiar el registro? " +dnip, "Eliminar Registro", JOptionPane.YES_NO_CANCEL_OPTION);
        switch (respuesta) {
            case JOptionPane.YES_OPTION:
            System.out.println("" + respuesta);
            break;
            case JOptionPane.NO_OPTION:

            break;
            case JOptionPane.CANCEL_OPTION:

            break;
        }

        if (respuesta == 0) {

            try {

                CallableStatement cs1 = ora2.getCon().getConexion().prepareCall("call p_elimclient(?)");
                cs1.setString(1,dnip);
                cs1.execute();

            } catch (Exception e) {
            }

        }
    }//GEN-LAST:event_btneliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int codp = Integer.parseInt(lbcodigo.getText());
        String dni,nombres,telfijo,telcel,email,ciudad ,direccion,nombrecont,sql;
        dni = dniField.getText();
        nombres = nombresField.getText();
        telfijo= telFijoField.getText();
        telcel = telCelularField.getText();
        email = emailField.getText();
        ciudad  = ciudadField.getText();
        direccion= direccionField.getText();
        nombrecont= nombreContactoField.getText();
        try {

            CallableStatement cs = ora2.getCon().getConexion().prepareCall("call mod_client(?,?,?,?,?,?,?,?,?)");
            cs.setInt(1,codp);
            cs.setString(2,dni);
            cs.setString(3,nombres);
            cs.setString(4,telfijo);
            cs.setString(5,telcel);
            cs.setString(6,email);
            cs.setString(7,ciudad);
            cs.setString(8,direccion);
            cs.setString(9,nombrecont);
            cs.execute();

        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnModificarActionPerformed

