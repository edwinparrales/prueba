    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int codp = Integer.parseInt(lbcod.getText());
        String nit,nom,telfijo,telcel,email,ciudad,dire,nomcon,sql;
        nit=nitField.getText();
        nom=nombreField.getText();
        
        telfijo=telFijoField.getText();
        telcel=telCelularField.getText();
        email=emailField.getText();
        nomcon=nombreContactoField.getText();
        ciudad=ciudadField.getText();
        dire=direccionField.getText();
        try {

            CallableStatement cs = ora2.getCon().getConexion().prepareCall("call p_modprovee(?,?,?,?,?,?,?,?,?)");
            cs.setInt(1,codp);
            cs.setString(2,nit);
            cs.setString(3,nom);
            cs.setString(4,telfijo);
            cs.setString(5,telcel);
            cs.setString(6,email);
            cs.setString(7,nomcon);
            cs.setString(8,ciudad);
            cs.setString(9,dire);
            cs.execute();

        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        String nitp = nitField.getText();
        int respuesta = JOptionPane.showConfirmDialog(null, " ¿Esta seguro que desea elminiar el registro? " + nitp, "Eliminar Registro", JOptionPane.YES_NO_CANCEL_OPTION);
        switch (respuesta) {
            case JOptionPane.YES_OPTION:
                System.out.println("" + respuesta);
                break;
            case JOptionPane.NO_OPTION:

                break;
            case JOptionPane.CANCEL_OPTION:

                break;
        }

        if (respuesta == 0) {

            try {

                CallableStatement cs1 = ora2.getCon().getConexion().prepareCall("call p_elimprovee(?)");
                cs1.setString(1,nitp);
                cs1.execute();

            } catch (Exception e) {
            }

        }

    }//GEN-LAST:event_btneliminarActionPerformed

