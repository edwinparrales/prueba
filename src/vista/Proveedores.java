    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // Realizar busqueda de registros en la tabla especialisatas

        String nitp= txtbusnit.getText();
        modta2 = new DefaultTableModel();

        ora2.consultar("select * from proveedores  where nit='"+nitp+"'");

        try {
            int numCol = ora2.getRstmdata().getColumnCount();
            for (int i = 0; i < numCol; i++) {
                modta2.addColumn(ora2.getRstmdata().getColumnLabel(i + 1));

            }

            while (ora2.getRst().next()) {
                Object[] fila = new Object[numCol];
                for (int i = 0; i < numCol; i++) {
                    fila[i] = ora2.getRst().getObject(i + 1);
                }

                modta2.addRow(fila);
                tablaProvee.setModel(modta2);
            }

        } catch (Exception e) {
        }finally{
            if(modta2.getRowCount()==0){
                JOptionPane.showMessageDialog(null," El registro "+nitp+" no se encuenrar ");
            }
            
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

